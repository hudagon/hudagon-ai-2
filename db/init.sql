create table category_color(
	id int unsigned primary key auto_increment,
    name varchar(60),
    updated_date datetime,
    created_date datetime,
    delete_status bit(1)
);

create table category_topic(
	id int unsigned primary key auto_increment,
    name varchar(60),
    updated_date datetime,
    created_date datetime,
    delete_status bit(1)
);

create table category_ratio(
	id int unsigned primary key auto_increment,
    name varchar(60),
    updated_date datetime,
    created_date datetime,
    delete_status bit(1)
);

create table category_subject(
	id int unsigned primary key auto_increment,
    name varchar(60),
    updated_date datetime,
    created_date datetime,
    delete_status bit(1)
);

create table category_style(
	id int unsigned primary key auto_increment,
    name varchar(60),
    updated_date datetime,
    created_date datetime,
    delete_status bit(1)
);

create table category_level_3(
	id bigint unsigned primary key auto_increment,
    category_subject_id int unsigned,
	category_style_id int unsigned,
    name varchar(60),
	level_3_desc varchar(30) null,
    updated_date datetime,
    created_date datetime,
    delete_status bit(1),
	foreign key (category_subject_id) references category_subject(id),
    foreign key (category_style_id) references category_style(id)
);

create table painting(
	id bigint unsigned primary key auto_increment,
    name varchar(300),
    img_url varchar(999),
    category_ratio_id int unsigned,
    category_topic_id int unsigned,
    category_subject_id int unsigned,
    category_style_id int unsigned,
    updated_date datetime,
    created_date datetime,
    delete_status bit(1),
	foreign key (category_ratio_id) references category_ratio(id),
    foreign key (category_topic_id) references category_topic(id),
	foreign key (category_style_id) references category_style(id),
    foreign key (category_subject_id) references category_subject(id)
);

create table category_level_3_painting(
	id bigint unsigned primary key auto_increment,
    painting_id bigint unsigned,
    category_level_3_id bigint unsigned,
    updated_date datetime,
    created_date datetime,
    delete_status bit(1),
	foreign key (painting_id) references painting(id),
    foreign key (category_level_3_id) references category_level_3(id)
);

create table painting_category_color(
	id bigint unsigned primary key auto_increment,
	painting_id bigint unsigned,
    category_color_id int unsigned,
    updated_date datetime,
    created_date datetime,
    delete_status bit(1),
	foreign key (painting_id) references painting(id),
    foreign key (category_color_id) references category_color(id)
);

